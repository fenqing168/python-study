# 运算符
a = 10;
# 赋值运算符
# =
b = a;
print('=:' + str(b));
# +=
b = 2;
b += a;
print('+=:' + str(b));

# -=
b = 2;
b -= a;
print('-=:' + str(b));

# *=
b = 2;
b *= a;
print('*=:' + str(b));

# /=
b = 2;
b /= a;
print('/=:' + str(b));

# //=
b = 2;
b //= a;
print('//=:' + str(b));

# %=
b = 2;
b %= a;
print('%=:' + str(b));

# **=
b = 2;
b **= a;
print('**=:' + str(b));
