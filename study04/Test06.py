# 运算符
# 成员运算符
listvar, strvar, tuplevar = [1, 2, 3], '123', {1, 2, 3}

# in
print(1 in listvar)
print('1' in strvar)
print(1 in tuplevar)

print(4 in listvar)
print('4' in strvar)
print(4 in tuplevar)

# not in
print(1 not in listvar)
print('1' not in strvar)
print(1 not in tuplevar)

print(4 not in listvar)
print('4' not in strvar)
print(4 not in tuplevar)